class WashModel {
  final String washId;

  final String washName;
  final String ownerName;

  final String phone;
  final String email;

  final String washType;

  final String city;
  final String address;

  WashModel({
    required this.washId,
    required this.washName,
    required this.ownerName,
    required this.phone,
    required this.email,
    required this.washType,
    required this.city,
    required this.address,
  });
}
